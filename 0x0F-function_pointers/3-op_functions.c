#include "3-calc.h"

/**
 * op_add - funtion that adds numbers
 * @a: number 1
 * @b: number 2
 * Return: sum a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - funtion subtracts numbers
 * @a : input 1
 * @b: input 2
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - funtion multiplies
 * @a: number 1
 * @b: number 2
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function divides
 * @a: input 1
 * @b: input 2
 * Return: division of a by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to mod
 * @a: number 1
 * @b: number 2
 * Return: remainder of the division of a
 */

int op_mod(int a, int b)
{
	return (a % b);
}
